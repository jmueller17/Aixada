<div class="faq-item">
	<h2>¿Cómo hago mi pedido?</h2>
	<a name="makeOrder"></a>
	<ol>
		<li>Hacer clic en "Siguente pedido"</li>
		<li>Seleccionar "Por proveedor" o "Por categoría de productos". Aparecerán los productos habilitados para la fecha indicada</li>
		<li>Completar las cantidades de cada producto y al hacer clic fuera o tabulador, la cantidad queda ingresada</li>
		<li>Finalmente en el panel de resumen (a la derecha), hacer clic en "Guardar"</li>
	<ol>
</div>
