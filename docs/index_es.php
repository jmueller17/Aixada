<div class="faq-item">
	<h2>¿Cómo imprimo mi pedido?</h2>
	<a name="printOrder"></a>
	<p>Para obtener una versión imprimible del pedido, basta hacer lo siguiente: </p>
        <ol>
                <li>Hacer clic en "Mi(s) pedido(s)"</li>
                <li>En la parte superior derecha de los pedidos, donde termina la línea del título, aparecerá un ícono de impresora. Al hacer clic en él, obtendrá una versión imprimible de su(s) pedido(s)</li>
        <ol>
</div>
